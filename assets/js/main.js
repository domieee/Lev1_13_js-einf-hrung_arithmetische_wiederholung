    // Der Subtraktions-Operator wird genutzt um Variablen und Summen zu subtrahieren

// Multiplikation
let multiplication_operator = 2 * 2;
console.log("multiplication: " + multiplication_operator);
    // Der Multiplikations-Operator wird genutzt um Variablen und Summen zu multiplizieren

// / / Division
let division_operator = 4 / 2;
console.log("division: " + division_operator);
    // Der Divisions-Operator wird genutzt um Variablen und Summen zu dividieren

// Modul: zeigt an, was der Rest sein würde.
let modulus_operator = 14 % 5;
console.log("modulus: " + modulus_operator);
/*
    Der Modulo Wert bezieht sich auf die Summe, die übrig bleibt (Restwert), wenn die gegebenen Summen dividiert werden
    14 % 5 => 14 / 5 = 2 * 5 = 10 > 10 - 14 = %4
 */
